package com.DTO.TiendaVirtualSB;

public class ClienteVO {
	
	 private Integer cedulaUsuario;
	 private String emailUsuario;
	 private String nombreUsuario;
	 private String password;
	 private String usuario;
	 private Integer flag;
	 
	 /**
	  * @return the cedulaUsuario
	  */
	 public Integer getcedulaUsuario() {
	  return cedulaUsuario;
	 }
	 
	 /**
	  * @param cedulaUsuario the cedulaUsuario to set
	  */
	 public void setcedulaUsuario(Integer cedulaUsuario) {
	  this.cedulaUsuario = cedulaUsuario;
	 }
	 
	 
	 /**
	  * @return the emailUsuario
	  */
	 public String getemailUsuario() {
	  return emailUsuario;
	 }
	 /**
	  * @param emailUsuario the emailUsuario to set
	  */
	 public void setemailUsuario(String emailUsuario) {
	  this.emailUsuario = emailUsuario;
	 }
	 
	 
	 /**
	  * @return the nombreUsuario
	  */
	 public String getnombreUsuario() {
	  return nombreUsuario;
	 }
	 /**
	  * @param nombreUsuario the nombreUsuario to set
	  */
	 public void setnombreUsuario(String nombreUsuario) {
	  this.nombreUsuario = nombreUsuario;
	 }
	 
	 /**
	  * @return the password
	  */
	 public String getpassword() {
	  return password;
	 }
	 /**
	  * @param password the password to set
	  */
	 public void setpassword(String password) {
	  this.password = password;
	 }
	 
	 /**
	  * @return the usuario
	  */
	 public String getusuario() {
	  return usuario;
	 }
	 /**
	  * @param usuario the usuario to set
	  */
	 public void setusuario(String usuario) {
	  this.usuario = usuario;
	 }
	 /**
	  * @return the flag
	  */
	 public Integer getflag() {
	  return flag;
	 }
	 /**
	  * @param usuario the flag to set
	  */
	 public void setflag(Integer flag) {
	  this.flag = flag;
	 }
	 

}
